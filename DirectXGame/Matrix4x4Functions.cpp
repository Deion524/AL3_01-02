/*==============================================
                3次元アフィン変換行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeAffineMatrix(const Vector3& scale, const Vector3& rotate, const Vector3& translate) {
	// 戻り値
	Matrix4x4 scaleMatrix;
	Matrix4x4 rotateXYMatrix;
	Matrix4x4 rotateXZMatrix;
	Matrix4x4 rotateYZMatrix;
	Matrix4x4 rotateMatrix;
	Matrix4x4 translateMatrix;
	Matrix4x4 resultMatrix;

	// 行列の計算(拡縮)
	scaleMatrix = MakeScaleMatrix(scale);

	// 行列の計算(回転YZ)
	rotateYZMatrix = MakeRotateXMatrix(rotate.x);
	// 行列の計算(回転XZ)
	rotateXZMatrix = MakeRotateYMatrix(rotate.y);
	// 行列の計算(回転XY)
	rotateXYMatrix = MakeRotateZMatrix(rotate.z);
	// 回転行列の結合
	rotateMatrix = Multiply(rotateYZMatrix, Multiply(rotateXZMatrix, rotateXYMatrix));

	// 行列の計算(移動)
	translateMatrix = MakeTranslateMatrix(translate);

	// アフィン変換(拡縮、回転)
	resultMatrix = Multiply(scaleMatrix, rotateMatrix);
	// アフィン変換(+移動)
	resultMatrix = Multiply(resultMatrix, translateMatrix);

	// 返却する値
	return resultMatrix;
}

/*==============================================
                    透視投影行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakePerspectiveFovMatrix(float fovY, float aspectRatio, float nearClip, float farClip) {
	// 戻り値
	Matrix4x4 result;

	// 行列の中身を初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}
	// 透視投影行列の作成
	result.m[0][0] = (1.0f / aspectRatio) * (1.0f / (tanf(fovY / 2.0f)));
	result.m[1][1] = 1.0f / (tanf(fovY / 2.0f));
	result.m[2][2] = farClip / (farClip - nearClip);
	result.m[2][3] = 1.0f;
	result.m[3][2] = (-nearClip * farClip) / (farClip - nearClip);

	// 返却する値
	return result;
}

/*==============================================
                    正射影行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeOrthographicMatrix(float left, float top, float right, float bottom, float nearClip, float farClip) {
	// 戻り値
	Matrix4x4 result;

	// 中身の初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}
	// 中身を代入
	result.m[0][0] = 2.0f / (right - left);
	result.m[1][1] = 2.0f / (top - bottom);
	result.m[2][2] = 1.0f / (farClip - nearClip);
	result.m[3][0] = (left + right) / (left - right);
	result.m[3][1] = (top + bottom) / (bottom - top);
	result.m[3][2] = nearClip / (nearClip - farClip);
	result.m[3][3] = 1.0f;

	// 返却する値
	return result;
}

/*==============================================
                ビューポート変換行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeViewportMatrix(float left, float top, float width, float height, float minDepth, float maxDepth) {
	// 戻り値
	Matrix4x4 result;

	// 中身の初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}
	// 中身を代入
	result.m[0][0] = width / 2.0f;
	result.m[1][1] = -height / 2.0f;
	result.m[2][2] = maxDepth - minDepth;
	result.m[3][0] = left + width / 2.0f;
	result.m[3][1] = top + height / 2.0f;
	result.m[3][2] = minDepth;
	result.m[3][3] = 1.0f;

	// 返却する値
	return result;
}

