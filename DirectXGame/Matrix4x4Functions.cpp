/*==============================================
                    転置行列
==============================================*/
Matrix4x4 Matrix4x4Functions::Transpose(const Matrix4x4& m) {
	Matrix4x4 result = m;

	// 1行目
	result.m[0][0] = m.m[0][0];
	result.m[0][1] = m.m[1][0];
	result.m[0][2] = m.m[2][0];
	result.m[0][3] = m.m[3][0];

	// 2行目
	result.m[1][0] = m.m[0][1];
	result.m[1][1] = m.m[1][1];
	result.m[1][2] = m.m[2][1];
	result.m[1][3] = m.m[3][1];

	// 3行目
	result.m[2][0] = m.m[0][2];
	result.m[2][1] = m.m[1][2];
	result.m[2][2] = m.m[2][2];
	result.m[2][3] = m.m[3][2];

	// 4行目
	result.m[3][0] = m.m[0][3];
	result.m[3][1] = m.m[1][3];
	result.m[3][2] = m.m[2][3];
	result.m[3][3] = m.m[3][3];

	return result;
}

/*==============================================
                    単位行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeIdentify4x4() {
	Matrix4x4 result;

	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}

	result.m[0][0] = 1.0f;
	result.m[1][1] = 1.0f;
	result.m[2][2] = 1.0f;
	result.m[3][3] = 1.0f;

	return result;
}

/*==============================================
                    平行移動行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeTranslateMatrix(const Vector3& translate) {
	// 戻り値
	Matrix4x4 result;

	// 行列の中身を初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}

	// 平行移動行列の作成
	result.m[0][0] = 1.0f;
	result.m[1][1] = 1.0f;
	result.m[2][2] = 1.0f;
	result.m[3][3] = 1.0f;

	result.m[3][0] = translate.x;
	result.m[3][1] = translate.y;
	result.m[3][2] = translate.z;

	// 返却する値
	return result;
}

/*==============================================
                    拡大縮小行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeScaleMatrix(const Vector3& scale) {
	// 戻り値
	Matrix4x4 result;

	// 行列の中身を初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}

	// 拡縮の計算
	result.m[0][0] = scale.x;
	result.m[1][1] = scale.y;
	result.m[2][2] = scale.z;
	result.m[3][3] = 1.0f;

	// 返却する値
	return result;
}

/*==============================================
                    座標変換
==============================================*/
Vector3 Matrix4x4Functions::Transform(const Vector3& vector, const Matrix4x4& matrix) {
	// 戻り値
	Vector3 result;

	// 計算
	result.x = vector.x * matrix.m[0][0] + vector.y * matrix.m[1][0] + vector.z * matrix.m[2][0] + 1.0f * matrix.m[3][0];
	result.y = vector.x * matrix.m[0][1] + vector.y * matrix.m[1][1] + vector.z * matrix.m[2][1] + 1.0f * matrix.m[3][1];
	result.z = vector.x * matrix.m[0][2] + vector.y * matrix.m[1][2] + vector.z * matrix.m[2][2] + 1.0f * matrix.m[3][2];
	float w = vector.x * matrix.m[0][3] + vector.y * matrix.m[1][3] + vector.z * matrix.m[2][3] + 1.0f * matrix.m[3][3];

	// アサーションチェック
	assert(w != 0.0f);
	result.x /= w;
	result.y /= w;
	result.z /= w;

	// 返却する値
	return result;
}

/*==============================================
                    X軸回転行列
==============================================*/
Matrix4x4 Matrix4x4Functions::MakeRotateXMatrix(float radian) {
	// 戻り値
	Matrix4x4 result;

	// 行列の中身を初期化
	for (int i = 0; i < 4; ++i) {
		for (int j = 0; j < 4; ++j) {
			result.m[i][j] = 0.0f;
		}
	}

	// 回転行列の作成
	result.m[0][0] = 1.0f;
	result.m[3][3] = 1.0f;

	result.m[1][1] = cosf(radian);
	result.m[2][1] = -sinf(radian);

	result.m[1][2] = sinf(radian);
	result.m[2][2] = cosf(radian);

	// 返却する値
	return result;
}

